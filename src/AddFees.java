   public int getReceiptNo()
   {
       int receiptno = 6;
       try{
            Connection conn=ConnectionProvider.getConnection();
            PreparedStatement pst=conn.prepareStatement("select max(rno) from studentfee");
            ResultSet rs=pst.executeQuery();
            if(rs.next()==true){
                receiptno=rs.getInt(1);
            }
       }
       catch(Exception e)
       {
           e.printStackTrace();
       }
       return receiptno+1;
   }
        
               
                                              
        
            
