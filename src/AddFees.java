    public boolean validation()
    {
        if(trfrom.getText().equals(""))
        {
            JOptionPane.showMessageDialog(this,"please enter the name of the student payed");
            return false;
        }
        if(tusn.getText().equals(""))
        {
            JOptionPane.showMessageDialog(this,"please enter the USN");
            return false;
        }
        if(tamount.getText().equals("") || tamount.getText().matches("[0-9]+")==false)
        {
            JOptionPane.showMessageDialog(this,"please enter amount in numbers");
             return false;
        }
           
        return true;
    }
    
    public void fillcomboBox(){
         try{
              Connection conn=ConnectionProvider.getConnection();
              PreparedStatement pst=conn.prepareStatement("select coursename from course");
              ResultSet rs=pst.executeQuery();
              while(rs.next()){
                  combocourse.addItem(rs.getString("coursename"));
              }
         }
        catch(Exception e){
                e.printStackTrace();
                }
         
    }
